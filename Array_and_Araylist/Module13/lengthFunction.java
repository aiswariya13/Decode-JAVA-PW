public class lengthFunction {
    public static void main(String[] args) {
        int n ;
        int[] arr = {10, 50, 14, 74, 85, 65, 14, 13, 10};
        n= arr.length;
        System.out.println(n);

        for(int i=0; i < n ; i++){
            System.out.print(arr[i] + " ");
        }

    }
}
