// Write a Java program that demonstrates the use of increment (++), decrement (--), and modulus (%) operators. Print the results.


public class question_4 {
    public static void main(String[] args) {
        int x = 10;
        System.out.println("Original value: " + x);
        int z = x + 1;
        System.out.println("After increment the value is: " + z);
        int  y = z - 1;
        System.out.println("After decrement the value is: "+ y);
        int remainder = z % x ;
        System.out.println("Remainder: "+remainder);



    }
    
}
